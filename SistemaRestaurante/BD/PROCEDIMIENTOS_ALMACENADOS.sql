CREATE PROCEDURE SP_Registro_HoraEntrada(
@idempl VARCHAR(8)
)
AS
BEGIN 
	INSERT INTO REGISTRO_JORNADA(HORA_ENTRADA,ID_EMPLEADO)
	VALUES (CONVERT(DATETIME, GETDATE() ),@idempl)
END