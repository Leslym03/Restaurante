

CREATE PROCEDURE Eliminar_Empleado(
@idemp VARCHAR(8)
)
AS
BEGIN
	DELETE FROM EMPLEADO_CODIGO
	WHERE ID_EMPLEADO =	@idemp;
END



CREATE PROCEDURE Eliminar_Mesa(
@idmes INT
)
AS
BEGIN
	DELETE FROM MESA
	WHERE ID_MESA =	@idmes;

	DBCC CHECKIDENT (MESA, RESEED);
END


CREATE PROCEDURE Eliminar_Proveedor(
@idprov INT
)
AS
BEGIN
	DELETE FROM PROVEEDOR
	WHERE ID_PROVEEDOR = @idprov;
END


CREATE PROCEDURE Eliminar_Insumo(
@idsum INT
)
AS
BEGIN
	DELETE FROM INSUMO
	WHERE ID_INSUMO = @idsum;
END


CREATE PROCEDURE Eliminar_Pedido(
@fecha VARCHAR
)
AS
BEGIN
	DELETE FROM PEDIDO
	WHERE FECHA_PEDIDO = CONVERT(DATETIME,@fecha);
END



CREATE PROCEDURE Eliminar_Producto(
@idprod INT
)
AS
BEGIN
	DELETE FROM PRODUCTO
	WHERE ID_PRODUCTO = @idprod;
END


CREATE PROCEDURE Eliminar_Comb(
@idcomb INT
)
AS
BEGIN
	DELETE FROM COMBO
	WHERE ID_COMBO = @idcomb;
END


CREATE PROCEDURE Eliminar_Orden(
@idorden INT
)
AS
BEGIN
	DELETE FROM ORDEN_CODIGO
	WHERE ID_ORDEN = @idorden;
END


CREATE PROCEDURE Eliminar_Reserv(
@fecha DATETIME
)
AS
BEGIN
	DELETE FROM RESERVACION
	WHERE FECHA_RESERVACION = @fecha;
END


